# CHAMFER #6 - MIDDLE (Y = 0)
chamfer6_p1 = (306.865, 0.0, 297.330)
chamfer6_p2 = (305.470, 0.0, 298.715)

# =========================================================
# COLLECT ALL CHAMFER TARGETS
# =========================================================
chamfer_targets = [
    ("#1 LEFT TOP (Y=+10)",    chamfer1_p1, chamfer1_p2),
    ("#2 LEFT BOTTOM (Y=-10)", chamfer2_p1, chamfer2_p2),
    ("#3 LEFT MIDDLE (Y=0)",   chamfer3_p1, chamfer3_p2),
    ("#4 RIGHT TOP (Y=+10)",   chamfer4_p1, chamfer4_p2),
    ("#5 RIGHT BOTTOM (Y=-10)", chamfer5_p1, chamfer5_p2),
    ("#6 RIGHT MIDDLE (Y=0)",  chamfer6_p1, chamfer6_p2),
]

# =========================================================
# BUILD PART
# =========================================================
with BuildPart() as bp:

    # =====================================================
    # MAIN PROFILE
    # =====================================================
    with BuildSketch(Plane.XZ):

        with BuildLine():
            Polyline(*sketch_pts, close=True)

        make_face()

    extrude(amount=EXTRUSION_DEPTH)

    # =====================================================
    # HOLE 1
    # =====================================================
    h1_center = (
        (hole1_p1[0] + hole1_p2[0]) / 2,
        (hole1_p1[1] + hole1_p2[1]) / 2,
        (hole1_p1[2] + hole1_p2[2]) / 2,
    )

    h1_dx = hole1_p2[0] - hole1_p1[0]
    h1_dz = hole1_p2[2] - hole1_p1[2]

    h1_angle = math.degrees(math.atan2(h1_dz, h1_dx))

    with Locations(Location(h1_center, (0, h1_angle, 0))):
        Cylinder(
            radius=HOLE_RADIUS,
            height=30,
            align=(Align.CENTER, Align.CENTER, Align.CENTER),
            mode=Mode.SUBTRACT,
        )

    # =====================================================
    # HOLE 2
    # =====================================================
    h2_center = (
        (hole2_p1[0] + hole2_p2[0]) / 2,
        (hole2_p1[1] + hole2_p2[1]) / 2,
        (hole2_p1[2] + hole2_p2[2]) / 2,
    )

    h2_dx = hole2_p2[0] - hole2_p1[0]
    h2_dz = hole2_p2[2] - hole2_p1[2]

    h2_angle = math.degrees(math.atan2(h2_dz, h2_dx))

    with Locations(Location(h2_center, (0, h2_angle, 0))):
        Cylinder(
            radius=HOLE_RADIUS,
            height=30,
            align=(Align.CENTER, Align.CENTER, Align.CENTER),
            mode=Mode.SUBTRACT,
        )

    # =====================================================
    # FIND ALL CHAMFER EDGES
    # =====================================================
    def close(p1, p2, tol=CHAMFER_DISTANCE_TOL):
        return (
            abs(p1[0] - p2[0]) < tol and
            abs(p1[1] - p2[1]) < tol and
            abs(p1[2] - p2[2]) < tol
        )

    all_chamfer_edges = []

    for edge in bp.edges():
        verts = edge.vertices()
        if len(verts) < 2:
            continue

        v1 = verts[0].to_tuple()
        v2 = verts[1].to_tuple()

        # Loop through all chamfer targets
        for name, target_p1, target_p2 in chamfer_targets:
            if (
                (close(v1, target_p1) and close(v2, target_p2)) or
                (close(v1, target_p2) and close(v2, target_p1))
            ):
                all_chamfer_edges.append(edge)
                print(f"✓ CHAMFER {name} edge found")
                break

    # =====================================================
    # APPLY ALL CHAMFERS (with auto-fallback for size)
    # =====================================================
    print(f"\nTotal edges found: {len(all_chamfer_edges)}/6")

    if all_chamfer_edges:
        applied = False
        for length in [CHAMFER_LENGTH, 0.5, 0.3]:
            try:
                chamfer(all_chamfer_edges, length=length)
                print(f"\n✓ {len(all_chamfer_edges)} CHAMFER(S) APPLIED at {length}mm")
                applied = True
                break
            except ValueError:
                print(f"✗ {length}mm too big, trying smaller...")

        if not applied:
            print("✗ All chamfer lengths failed")
    else:
        print("✗ NO CHAMFER EDGES FOUND")

# =========================================================
# FINAL MODEL
# =========================================================
model = bp.part

