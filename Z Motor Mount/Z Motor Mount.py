# =====================================================
# MIRROR FUNCTION
# =====================================================
def mirror_point_3d(point, normal, d):

    p = np.array(point)

    dist = np.dot(normal, p) - d

    mirrored = p - 2 * dist * normal

    return mirrored.tolist()

